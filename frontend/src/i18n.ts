export type LangKey = keyof typeof I18N;

export function getLang(): LangKey {
  const stored = (typeof window !== 'undefined') ? localStorage.getItem('aura_lang') : null;
  if (stored && (stored in I18N)) return stored as LangKey;
  return 'en';
}
